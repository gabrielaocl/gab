def observer(raw_output, error):
    if error:
        return f"Error occurred: {error}"
    else:
        return f"Execution result: {raw_output}"

def get_table_path(task):
    return f"/content/drive/MyDrive/Project 3/da-dev-tables/{task['file_name']}"

MAX_STEPS = 5

def run_agent(task):
    history = ""
    question = task["question"]
    table_path = get_table_path(task)

    print("USING TABLE PATH:", table_path)

    for step in range(MAX_STEPS):
        print(f"\nSTEP {step+1}")

        planner_prompt = f"""
You are a planner for a CSV data-analysis agent.

Question: {question}
CSV path: {table_path}
Constraints: {task.get('constraints', '')}
Required output format: {task.get('format', '')}

History:
{history}

Return JSON with:
- thought
- is_done
- response

Keep the response short.
If enough information is available, finish.
"""
        plan = planner_step(planner_prompt)
        print("PLAN:", plan)

        if plan.is_done:
            return plan.response

        code = coder(
            plan.response,
            question,
            table_path,
            task.get("constraints", ""),
            task.get("format", "")
        )

        output, error, cleaned_code = executor(code)

        print("CODE:\n", cleaned_code)

        obs = f"Execution error: {error}" if error else f"Execution result: {output}"
        print("OBSERVATION:", obs)

        history += f"""
Instruction: {plan.response}
Code:
{cleaned_code}
Observation: {obs}
"""

    return "max steps reached"

