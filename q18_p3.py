from pydantic import BaseModel

class PlannerOutput(BaseModel):
    thought: str
    is_done: bool
    response: str

def planner_step(prompt):
    json_string_result = model(prompt, PlannerOutput, max_new_tokens=800)
    parsed_dict = json.loads(json_string_result)
    return PlannerOutput(**parsed_dict)

